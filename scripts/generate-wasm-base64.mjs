// Generates src/util/wasm-binary.ts (the WASM bytes as a base64 string)
// AND src/util/pg-wasm-shim.js (a patched copy of pg-wasm's web/index.js).
//
// The shim is necessary because wasm-bindgen's web target leaves a dead
// default-value branch in __wbg_init:
//
//   if (module_or_path === undefined) {
//       module_or_path = new URL('index_bg.wasm', import.meta.url);
//   }
//
// `loadWasm` always passes a defined `module_or_path` (the decoded base64
// bytes) so the branch never fires at runtime. But webpack 5 statically
// analyses `new URL(..., import.meta.url)` as an asset import and fails
// the consumer build because no separate `index_bg.wasm` file ships in
// pg-js's dist.
//
// Run as a prebuild step: node scripts/generate-wasm-base64.mjs

import { readFileSync, writeFileSync, existsSync } from 'fs';

const wasmCandidates = [
  'node_modules/@e4a/pg-wasm/web/index_bg.wasm',
  'node_modules/@e4a/pg-wasm/bundler/index_bg.wasm',
];

const wasmPath = wasmCandidates.find((p) => existsSync(p));
if (!wasmPath) {
  console.error('Could not find index_bg.wasm');
  process.exit(1);
}

const bytes = readFileSync(wasmPath);
const b64 = bytes.toString('base64');

writeFileSync(
  'src/util/wasm-binary.ts',
  `// Auto-generated by scripts/generate-wasm-base64.mjs — do not edit\nexport const WASM_BASE64 = "${b64}";\n`
);

console.log(`Generated wasm-binary.ts (${(b64.length / 1024).toFixed(0)} KB)`);

// --- Patched shim ---

const shimSource = wasmPath.replace('index_bg.wasm', 'index.js');
if (!existsSync(shimSource)) {
  console.error(`Could not find pg-wasm shim at ${shimSource}`);
  process.exit(1);
}

const shimSrc = readFileSync(shimSource, 'utf8');

// Strip the dead default-value branch. The exact whitespace varies a bit
// across wasm-bindgen versions, so the regex is forgiving — but if it
// stops matching entirely we should fail loudly so we notice when the
// upstream shim shape changes.
const deadBranchRe =
  /if\s*\(\s*module_or_path\s*===\s*undefined\s*\)\s*\{\s*module_or_path\s*=\s*new\s+URL\(\s*['"]index_bg\.wasm['"]\s*,\s*import\.meta\.url\s*\)\s*;\s*\}\s*/;
if (!deadBranchRe.test(shimSrc)) {
  console.error(
    'pg-wasm shim does not contain the expected `new URL("index_bg.wasm", ' +
      'import.meta.url)` dead branch. Either wasm-bindgen now ships clean ' +
      'output (great — drop this script step) or the regex above needs ' +
      'updating to match the new wasm-bindgen output shape.'
  );
  process.exit(1);
}

const patched = shimSrc.replace(deadBranchRe, '');

writeFileSync(
  'src/util/pg-wasm-shim.js',
  '// Auto-generated by scripts/generate-wasm-base64.mjs — do not edit\n' +
    '// Source: ' +
    shimSource +
    ' with the dead `new URL("index_bg.wasm", import.meta.url)`\n' +
    '// branch in __wbg_init removed so webpack does not try to resolve it.\n' +
    patched
);

console.log(
  `Generated pg-wasm-shim.js (${(patched.length / 1024).toFixed(0)} KB)`
);
